	getElementsByTagName(); 通过标签名称来得到对象 ，得到集合*****
	
	getElementsByClassName() 通过元素的class属性来得到对象******
	
	getElementsByName() 通过标记的name属性来得到对象，集合。
 	
	获得页面元素的。*************
	
