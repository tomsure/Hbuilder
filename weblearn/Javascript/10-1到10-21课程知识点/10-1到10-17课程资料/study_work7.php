一 名词解释
1 对象：把一切事物多看做对象，一切皆对象，以面向对象的形式编程。
2 面向对象（oop）
	1》对象的属性和方法：
		属性：事物的特性  理解：事物本身自带的 “有”；
		方法：事物的功能 理解：事物本省 “能”

