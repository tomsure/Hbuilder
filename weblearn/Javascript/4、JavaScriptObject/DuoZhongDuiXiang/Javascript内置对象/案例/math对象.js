//1,Math.abs()求绝对值，用Math方法加点加abs() 
var n=-9;
alert(Math.abs(n))
//2,Math.round()四舍五入  Math.round
var n1=3.14;
alert(Math.round(n1));
//3. Math.ceil(),进一取整，只要有小数，则小数去掉，整数加1；
var n2=3.142;
alert(Math.ceil(n2));
//4.Math.floor(),只要有小数，则小数去掉，取整数。
var n3=3.33;
alert(Math.floor(n3));
//4.Math.random(),取0到1之间的随机数；
alert(Math.random())//这里直接输出不需要变量，数值随机变化；

//5.max和min 求最大数和最小数
alert(Math.max(1,2,3,4,5,6,7,8,9))
alert(Math.min(2,5,6,7,8,9))
